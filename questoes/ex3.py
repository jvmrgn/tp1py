# As principais diferenças entre listas, dicionários e tuplas em Python:

# **Lista**: 
# - É uma coleção ordenada e que pode ser modificada. 
# - Perfeita para guardar sequências de dados.
# - Por exemplo, podemos ter uma lista de usuários do INFwebNET:
# usuarios = ["João", "Ana", "Carlos", "Mariana"]

# **Dicionário**: 
# - Funciona como uma coleção de pares chave-valor, onde cada chave é única.
# - É ótimo para organizar dados de forma estruturada.
# - Um exemplo seria guardar as informações de cada usuário, assim:
# perfil_usuario = {
#     "nome": "João",
#     "idade": 25,
#     "localização": ("São Paulo", "SP")
# }

# **Tupla**: 
# - Semelhante às listas, mas com a diferença de ser imutável; ou seja, não dá para alterar depois de criada.
# - É útil para agrupar dados que não precisam mudar.
# - Um exemplo disso seria guardar a localização de um usuário:
# localizacao_usuario = ("São Paulo", "SP")

# Resumindo: listas são ideais para coleções que podem mudar, 
# dicionários são para dados associados, e 
# tuplas são boas para agrupamentos que permanecem constantes.

